package pl.krystian;

public class MailFrame {

	private String to;
	private String title;
	private String text;
	private boolean isHttpActive;
	
	
	
	
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public boolean isHttpActive() {
		return isHttpActive;
	}
	public void setHttpActive(boolean isHttpActive) {
		this.isHttpActive = isHttpActive;
	}
	
	
	
	
}
